public class BitacoraCargaAutomaticaPersonaModel {
	private Integer idCarga;
	private Integer idEmpresa;
	private String numeroOficio;
	private String tipoOficio;
	private String cuenta;
	private Integer numeroConsecutivoPersona;
	private String observaciones;
	
	public Integer getIdCarga() {
		return idCarga;
	}
	public void setIdCarga(Integer idCarga) {
		this.idCarga = idCarga;
	}
	public Integer getIdEmpresa() {
		return idEmpresa;
	}
	public void setIdEmpresa(Integer idEmpresa) {
		this.idEmpresa = idEmpresa;
	}
	public String getNumeroOficio() {
		return numeroOficio;
	}
	public void setNumeroOficio(String numeroOficio) {
		this.numeroOficio = numeroOficio;
	}
	public String getTipoOficio() {
		return tipoOficio;
	}
	public void setTipoOficio(String tipoOficio) {
		this.tipoOficio = tipoOficio;
	}
	public Integer getNumeroConsecutivoPersona() {
		return numeroConsecutivoPersona;
	}
	public void setNumeroConsecutivoPersona(Integer numeroConsecutivoPersona) {
		this.numeroConsecutivoPersona = numeroConsecutivoPersona;
	}
	public String getObservaciones() {
		return observaciones;
	}
	public void setObservaciones(String observaciones) {
		this.observaciones = observaciones;
	}
	public String getCuenta() {
		return cuenta;
	}
	public void setCuenta(String cuenta) {
		this.cuenta = cuenta;
	}
}