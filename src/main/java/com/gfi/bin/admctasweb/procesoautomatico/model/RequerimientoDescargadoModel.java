package com.gfi.bin.admctasweb.procesoautomatico.model;

import java.util.Date;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

import com.gfi.bin.admctasweb.util.Constantes;

/**
 * 
 * @author ESS3ESPP
 *
 */
@XmlRootElement(name=Constantes.TAG_REQUERIMIENTO_DESCARGADO)
@XmlAccessorType(XmlAccessType.FIELD)
public class RequerimientoDescargadoModel {
	
	@XmlElement(name=Constantes.TAG_RD_ID_FOLIO)
	private String idFolio;
	
	@XmlElement(name=Constantes.TAG_RD_ANIO)
	private String anio;
	
	@XmlElement(name=Constantes.TAG_RD_AREA)
	private String area;
	
	@XmlElement(name=Constantes.TAG_RD_NO_OFICIO)
	private String noOficio;
	
	@XmlElement(name=Constantes.TAG_RD_NO_EXPEDIENTE)
	private String noExpediente;
	
	@XmlElement(name=Constantes.TAG_RD_FECHA_PUBLICACION)
	private Date fechaPublicacion;
	
	@XmlElement(name=Constantes.TAG_RD_PLAZO_RESPUESTA)
	private Integer plazoRespuesta;
	
	@XmlElement(name=Constantes.TAG_RD_OFICIO_FILE_NAME)
	private String oficioFileName;
	
	@XmlElement(name=Constantes.TAG_RD_REQUERIMIENTO_FILE_NAME)
	private String requerimientoFileName;
	
	@XmlElement(name=Constantes.TAG_RD_XML_FILE_NAME)
	private String xmlFileName;

	public String getIdFolio() {
		return idFolio;
	}

	public void setIdFolio(String idFolio) {
		this.idFolio = idFolio;
	}

	public String getAnio() {
		return anio;
	}

	public void setAnio(String anio) {
		this.anio = anio;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getNoOficio() {
		return noOficio;
	}

	public void setNoOficio(String noOficio) {
		this.noOficio = noOficio;
	}

	public String getNoExpediente() {
		return noExpediente;
	}

	public void setNoExpediente(String noExpediente) {
		this.noExpediente = noExpediente;
	}

	public Date getFechaPublicacion() {
		return fechaPublicacion;
	}

	public void setFechaPublicacion(Date fechaPublicacion) {
		this.fechaPublicacion = fechaPublicacion;
	}

	public Integer getPlazoRespuesta() {
		return plazoRespuesta;
	}

	public void setPlazoRespuesta(Integer plazoRespuesta) {
		this.plazoRespuesta = plazoRespuesta;
	}

	public String getOficioFileName() {
		return oficioFileName;
	}

	public void setOficioFileName(String oficioFileName) {
		this.oficioFileName = oficioFileName;
	}

	public String getRequerimientoFileName() {
		return requerimientoFileName;
	}

	public void setRequerimientoFileName(String requerimientoFileName) {
		this.requerimientoFileName = requerimientoFileName;
	}

	public String getXmlFileName() {
		return xmlFileName;
	}

	public void setXmlFileName(String xmlFileName) {
		this.xmlFileName = xmlFileName;
	}
}