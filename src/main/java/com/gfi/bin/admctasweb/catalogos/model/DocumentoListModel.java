package com.gfi.bin.admctasweb.catalogos.model;

import java.io.Serializable;
import java.util.List;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;

/**
 * @author lugl4884
 *
 */
@JsonIgnoreProperties(ignoreUnknown = true)
public class DocumentoListModel implements Serializable{
	
	private static final long serialVersionUID = 1L;
	
	private String numOficio;
	private String tipoOficio;
	private Long numDocto;
	private List<DocumentoModel> documentoList;
	
	
	/**
	 * @return the numOficio
	 */
	public String getNumOficio() {
		return numOficio;
	}
	/**
	 * @param numOficio the numOficio to set
	 */
	public void setNumOficio(String numOficio) {
		this.numOficio = numOficio;
	}
	/**
	 * @return the tipoOficio
	 */
	public String getTipoOficio() {
		return tipoOficio;
	}
	/**
	 * @param tipoOficio the tipoOficio to set
	 */
	public void setTipoOficio(String tipoOficio) {
		this.tipoOficio = tipoOficio;
	}
	/**
	 * @return the numDocto
	 */
	public Long getNumDocto() {
		return numDocto;
	}
	/**
	 * @param numDocto the numDocto to set
	 */
	public void setNumDocto(Long numDocto) {
		this.numDocto = numDocto;
	}
	/**
	 * @return the documentoList
	 */
	public List<DocumentoModel> getDocumentoList() {
		return documentoList;
	}
	/**
	 * @param documentoList the documentoList to set
	 */
	public void setDocumentoList(List<DocumentoModel> documentoList) {
		this.documentoList = documentoList;
	}
	

}
