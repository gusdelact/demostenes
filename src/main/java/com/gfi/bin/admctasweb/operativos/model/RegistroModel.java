package com.gfi.bin.admctasweb.operativos.model;

import java.util.List;

import org.codehaus.jackson.annotate.JsonIgnoreProperties;

import com.gfi.bin.admctasweb.catalogos.model.PersonaModel;

/**
 * @author lugl4884
 *
 */
@JsonIgnoreProperties(ignoreUnknown = true)
public class RegistroModel {
	
	private String numOficio;
	private String tipoOficio;
	private List<PersonaModel> listPer;
	
	/**
	 * @return the numOficio
	 */
	public String getNumOficio() {
		return numOficio;
	}
	/**
	 * @param numOficio the numOficio to set
	 */
	public void setNumOficio(String numOficio) {
		this.numOficio = numOficio;
	}
	/**
	 * @return the tipoOficio
	 */
	public String getTipoOficio() {
		return tipoOficio;
	}
	/**
	 * @param tipoOficio the tipoOficio to set
	 */
	public void setTipoOficio(String tipoOficio) {
		this.tipoOficio = tipoOficio;
	}
	/**
	 * @return the listPer
	 */
	public List<PersonaModel> getListPer() {
		return listPer;
	}
	/**
	 * @param listPer the listPer to set
	 */
	public void setListPer(List<PersonaModel> listPer) {
		this.listPer = listPer;
	}

}
