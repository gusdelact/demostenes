package com.gfi.bin.admctasweb.comunes.enums;

/**
 * Enumeración con tipos de Solicitudes para requerimientos
 * @author ESS3VAVC
 *
 */
public enum SolicitudEnum {

	TOTAL("TOT", "Total"), 
	PARCIAL( "PAR", "Parcial"),
	UNA_CUENTA("UCTA", "De una Cuenta del Cliente"),
	DOS_CUENTAS("DCTA", "De dos o más Cuentas del Cliente");	
	
	
	
	private SolicitudEnum(String clave, String descripcion) {
		this.clave=clave;
		this.descripcion=descripcion;
	}
	
	public String getClave() {
		return clave;
	}	

	public String getDescripcion() {
		return descripcion;
	}	

	
	private String descripcion;	
	private String clave;	
	
	
}
