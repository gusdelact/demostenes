package com.gfi.corp.component.genericgridman.dto;


/**
 * Objeto con los parametros para la exportacion del Grid.
 * @author LUGL4884
 *
 */
public class ExportarGridReq {
	
	private String cveGrid;
	private String tipo;
	private String cveUsuario;
	private String nombre;
	private String extParam;
	private GenericGridReq gridParams;
	
	
	/**
	 * @return the cveGrid
	 */
	public String getCveGrid() {
		return cveGrid;
	}
	/**
	 * @param cveGrid the cveGrid to set
	 */
	public void setCveGrid(String cveGrid) {
		this.cveGrid = cveGrid;
	}
	/**
	 * @return the tipo
	 */
	public String getTipo() {
		return tipo;
	}
	/**
	 * @param tipo the tipo to set
	 */
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	/**
	 * @return the cveUsuario
	 */
	public String getCveUsuario() {
		return cveUsuario;
	}
	/**
	 * @param cveUsuario the cveUsuario to set
	 */
	public void setCveUsuario(String cveUsuario) {
		this.cveUsuario = cveUsuario;
	}
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the extParam
	 */
	public String getExtParam() {
		return extParam;
	}
	/**
	 * @param extParam the extParam to set
	 */
	public void setExtParam(String extParam) {
		this.extParam = extParam;
	}
	/**
	 * @return the gridParams
	 */
	public GenericGridReq getGridParams() {
		return gridParams;
	}
	/**
	 * @param gridParams the gridParams to set
	 */
	public void setGridParams(GenericGridReq gridParams) {
		this.gridParams = gridParams;
	}

}
